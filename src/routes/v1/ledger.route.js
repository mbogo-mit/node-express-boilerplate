const express = require('express');
const validate = require('../../middlewares/validate');
const validator = require('../../validations/ledger.validation');
const controller = require('../../controllers/ledger.controller');
const { traverseObjWithKeys, convertEndpointUrlToObjKeys } = require('../../utils/utils');
// eslint-disable-next-line no-unused-vars
const logger = require('../../config/logger');

const router = express.Router();

const endpoints = [
  '/environmentvariables/api',
  '/environmentvariables/ec2/:ec2nameID',
  '/environmentvariables/apigateway/:apigatewaynameID',
  '/environmentvariables/websocket/:websocketnameID',
  '/environmentvariables/mongodb/:mongodbnameID',
  '/environmentvariables/nextjs/:nextjsnameID',
  '/secrets/api',
  '/secrets/ec2/:ec2nameID',
  '/secrets/apigateway/:apigatewaynameID',
  '/secrets/websocket/:websocketnameID',
  '/secrets/mongodb/:mongodbnameID',
  '/secrets/nextjs/:nextjsnameID',
  '/events/api',
  '/events/graphqlscripts/:graphqlscriptsnameID',
  '/events/ec2/:ec2nameID',
  '/events/apigateway/:apigatewaynameID',
  '/events/websocket/:websocketnameID',
  '/events/mongodb/:mongodbnameID',
  '/events/nextjs/:nextjsnameID',
  '/architecture/api',
  '/architecture/ec2/:ec2nameID',
  '/architecture/apigateway/:apigatewaynameID',
  '/architecture/websocket/:websocketnameID',
  '/architecture/mongodb/:mongodbnameID',
  '/architecture/nextjs/:nextjsnameID',
];

// convert endpoints to router posts

// eslint-disable-next-line no-unused-vars
endpoints.map((endpoint, i) => {
  const keys = convertEndpointUrlToObjKeys(endpoint);
  logger.info(Object.keys(controller));
  const callback = traverseObjWithKeys(controller, keys);
  logger.info(`i: ${i} ${callback}`);
  router.post(endpoint, validate(traverseObjWithKeys(validator, keys)), callback);
  return null;
});

module.exports = router;

